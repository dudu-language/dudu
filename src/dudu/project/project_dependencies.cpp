std::filesystem::path dependency_cache_root(const ProjectConfig& config) {
    const std::filesystem::path root =
        config.project_dir.empty() ? std::filesystem::current_path() : config.project_dir;
    return root / ".dudu" / "deps";
}

std::filesystem::path dependency_package_root(const ProjectConfig& config,
                                              const ProjectDependency& dependency) {
    if (dependency.kind == "path") {
        return project_path(config, dependency.path);
    }
    if (dependency.kind == "git") {
        return dependency_cache_root(config) / dependency.name;
    }
    return {};
}

std::filesystem::path dependency_source_root(const std::filesystem::path& package_root) {
    const std::filesystem::path src = package_root / "src";
    return std::filesystem::exists(src) ? src : package_root;
}

void validate_dependency_package_root(const std::string& name, const std::filesystem::path& root) {
    if (!std::filesystem::exists(root / "dudu.toml")) {
        fail_dependency("Dudu dependency '" + name +
                        "' package root is missing dudu.toml: " + root.string());
    }
}

std::map<std::string, std::filesystem::path> dependency_module_roots(const ProjectConfig& config) {
    std::map<std::string, std::filesystem::path> out;
    for (const auto& [name, dependency] : config.dependencies) {
        const std::filesystem::path root = dependency_package_root(config, dependency);
        if (!root.empty()) {
            out[name] = dependency_source_root(root);
        }
    }
    return out;
}

void ensure_project_dependencies(ProjectConfig& config, bool update, bool quiet) {
    for (auto& [name, dependency] : config.dependencies) {
        const std::filesystem::path root = dependency_package_root(config, dependency);
        if (dependency.kind == "path") {
            if (!std::filesystem::exists(root)) {
                fail_dependency("Dudu dependency '" + name + "' path not found: " + root.string());
            }
            validate_dependency_package_root(name, root);
            dependency.resolved_root = root;
            continue;
        }
        if (dependency.kind == "git") {
            ensure_git_dependency(dependency, root, update, quiet);
            validate_dependency_package_root(name, root);
            continue;
        }
        fail_dependency("Dudu dependency '" + name + "' has unknown kind: " + dependency.kind);
    }
    write_lock_if_changed(config);
}

void merge_dependency_native_inputs(ProjectConfig& config) {
    for (const auto& [name, dependency] : config.dependencies) {
        const std::filesystem::path root = dependency_package_root(config, dependency);
        validate_dependency_package_root(name, root);
        const ProjectConfig package = parse_project_config(root / "dudu.toml");
        merge_native_inputs(config, package);
    }
}

